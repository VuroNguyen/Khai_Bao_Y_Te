export const getDay = (day) => {
    switch(day){
        case 0:{
            return 'Chủ Nhật'
            break;
        }
        case 1:{
            return 'Thứ hai'
            break;
        }
        case 2:{
            return 'Thứ ba'
            break;
        }
        case 3:{
            return 'Thứ tư'
            break;
        }
        case 4:{
            return 'Thứ năm'
            break;
        }
        case 5:{
            return 'Thứ sáu'
            break;
        }
        case 6:{
            return 'Thứ bảy'
            break;
        }
    }
}
